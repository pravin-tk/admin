package org.school.admin.data;

public class SchoolAnalyticsData {
	private Integer schoolId;
	private Integer contactClicks;
	
	public Integer getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(Integer schoolId) {
		this.schoolId = schoolId;
	}
	public Integer getContactClicks() {
		return contactClicks;
	}
	public void setContactClicks(Integer contactClicks) {
		this.contactClicks = contactClicks;
	}
	
}